// using let and const and var
// let value are changeable
let name = "Sarkar";
name = "Amit Sarkar";
console.log(name);

// const are fixed and we can't reassign it's value
const country = "Bangladesh";
console.log(country);
// use of if else statement
const num = 20;
if (num > 20) {
    console.log("Yes\n");
} else {
    console.log("No");
}